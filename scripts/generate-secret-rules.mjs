import { readFileSync, writeFileSync } from "node:fs";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const sourcePath = join(root, "shared/canonical-secret-rules.json");
const nodePath = join(root, "src/generated-secret-rules.ts");
const nodeCorpusPath = join(root, "tests/generated-secret-rule-corpus.ts");
const swiftPath = join(root, "apps/neondiff-desktop/Sources/NeonDiffDesktopCore/Services/CanonicalSecretRules.generated.swift");
const source = JSON.parse(readFileSync(sourcePath, "utf8"));
const ASCII_LEFT_BOUNDARY = "(?<![A-Za-z0-9_])";
const ASCII_RIGHT_BOUNDARY = "(?![A-Za-z0-9_])";
const ECMASCRIPT_WHITESPACE_CONTENT = "\\u0009-\\u000D \\u00A0\\u1680\\u2000-\\u200A\\u2028\\u2029\\u202F\\u205F\\u3000\\uFEFF";
const ECMASCRIPT_WHITESPACE = `[${ECMASCRIPT_WHITESPACE_CONTENT}]`;

function foldAsciiCase(sourcePattern) {
  let inClass = false;
  let escaped = false;
  let output = "";
  for (const character of sourcePattern) {
    if (escaped) {
      output += `\\${character}`;
      escaped = false;
    } else if (character === "\\") {
      escaped = true;
    } else if (character === "[") {
      inClass = true;
      output += character;
    } else if (character === "]") {
      inClass = false;
      output += character;
    } else if (!inClass && /[A-Za-z]/.test(character)) {
      output += `[${character.toUpperCase()}${character.toLowerCase()}]`;
    } else {
      output += character;
    }
  }
  if (escaped) output += "\\";
  return output.replace(/\[([^\]]*)\]/g, (whole, content) =>
    content.includes("A-Z") && !content.includes("a-z")
      ? `[${content.replace("A-Z", "A-Za-z")}]`
      : whole
  );
}

function normalizePattern(rule) {
  let pattern = rule.source
    .replace("[\\s\\S]", "[\\u0000-\\uFFFF]")
    .replaceAll("[^/\\s@]", `[^/${ECMASCRIPT_WHITESPACE_CONTENT}@]`)
    .replaceAll("[^/\\s]", `[^/${ECMASCRIPT_WHITESPACE_CONTENT}]`)
    .replaceAll("[\\d ().-]", "[0-9 ().-]")
    .replaceAll("\\d", "[0-9]")
    .replaceAll("\\s", ECMASCRIPT_WHITESPACE);
  if (pattern.includes("\\b")) {
    const unbounded = pattern;
    pattern = unbounded.replaceAll("\\b", (_match, offset) =>
      offset === 0 || unbounded[offset - 1] === "(" ? ASCII_LEFT_BOUNDARY : ASCII_RIGHT_BOUNDARY
    );
  }
  if (rule.ignoreCase) pattern = foldAsciiCase(pattern);
  return { id: rule.id, source: pattern, ignoreCase: false };
}

const runtimeRules = source.rules.map(normalizePattern);
const runtimeCookieRule = {
  id: source.cookieHeader.id,
  prefix: source.cookieHeader.prefix,
  sensitiveNameSource: foldAsciiCase(source.cookieHeader.sensitiveNameSource),
  maximumAttributes: source.cookieHeader.maximumAttributes
};
const typescriptFixtureText = (fixture) => fixture.sampleParts
  ? `[${fixture.sampleParts.map((part) => JSON.stringify(part)).join(", ")}].join("")`
  : JSON.stringify(fixture.sample);

const banner = "// Generated by scripts/generate-secret-rules.mjs from shared/canonical-secret-rules.json. DO NOT EDIT.\n";
const node = `${banner}export interface CanonicalSecretRule { readonly id: string; readonly source: string; readonly ignoreCase: boolean; }\n`+
  `export const canonicalSecretRules: readonly CanonicalSecretRule[] = ${JSON.stringify(runtimeRules, null, 2)};\n`+
  `export const canonicalSensitiveCookieRule = ${JSON.stringify(runtimeCookieRule, null, 2)} as const;\n`+
  `export const canonicalSecretSafeLiterals: readonly string[] = ${JSON.stringify(source.safeLiterals, null, 2)};\n`;
const nodeCorpus = `${banner}export const canonicalSecretRuleCorpus = {\n  ruleIds: ${JSON.stringify([...source.rules.map((rule) => rule.id), source.cookieHeader.id], null, 2)},\n  sensitive: [\n${[...source.rules, source.cookieHeader].map((fixture) => `    { id: ${JSON.stringify(fixture.id)}, text: ${typescriptFixtureText(fixture)} },`).join("\n")}\n  ],\n  benign: ${JSON.stringify(source.benign, null, 2)}\n} as const;\n`;

const swiftString = (value) => JSON.stringify(value.replaceAll("\\/", "/"));
const swiftFixtureText = (fixture) => fixture.sampleParts
  ? `[${fixture.sampleParts.map(swiftString).join(", ")}].joined()`
  : swiftString(fixture.sample);
const swift = `${banner}import Foundation\n\n`+
  `extension CanonicalSecretRule {\n    static let generated: [CanonicalSecretRule] = [\n${runtimeRules.map((rule) => `        CanonicalSecretRule(id: ${swiftString(rule.id)}, source: ${swiftString(rule.source)}, ignoreCase: ${rule.ignoreCase}),`).join("\n")}\n    ]\n}\n\n`+
  `extension CanonicalSensitiveCookieRule {\n    static let generated = CanonicalSensitiveCookieRule(id: ${swiftString(runtimeCookieRule.id)}, prefix: ${swiftString(runtimeCookieRule.prefix)}, sensitiveNameSource: ${swiftString(runtimeCookieRule.sensitiveNameSource)}, maximumAttributes: ${runtimeCookieRule.maximumAttributes})\n}\n\n`+
  `enum CanonicalSecretSafeLiterals {\n    static let generated = [${source.safeLiterals.map(swiftString).join(", ")}]\n}\n\n#if DEBUG\n@_spi(Testing) public enum CanonicalSecretRuleCorpus {\n    public static let ruleIDs = [${[...source.rules.map((rule) => rule.id), source.cookieHeader.id].map(swiftString).join(", ")}]\n    public static let sensitive: [(id: String, text: String)] = [\n${[...source.rules, source.cookieHeader].map((fixture) => `        (${swiftString(fixture.id)}, ${swiftFixtureText(fixture)}),`).join("\n")}\n    ]\n    public static let benign: [(id: String, text: String)] = [\n${source.benign.map((fixture) => `        (${swiftString(fixture.id)}, ${swiftString(fixture.text)}),`).join("\n")}\n    ]\n}\n#endif\n`;

const outputs = [[nodePath, node], [nodeCorpusPath, nodeCorpus], [swiftPath, swift]];
if (process.argv.includes("--check")) {
  const stale = outputs.filter(([path, expected]) => {
    try { return readFileSync(path, "utf8") !== expected; } catch { return true; }
  });
  if (stale.length > 0) {
    console.error(`Generated secret rules are stale: ${stale.map(([path]) => path.slice(root.length + 1)).join(", ")}`);
    process.exit(1);
  }
  execFileSync(process.execPath, [join(root, "scripts/check-secret-rule-differential.mjs"), "--require-swift"], {
    cwd: root,
    stdio: "inherit"
  });
} else {
  for (const [path, content] of outputs) writeFileSync(path, content);
}
